// Atribuição

let x = 5 // Estou atribuindo o valor "5" a minha variável x.
let y = 2 // Estou atribuindo o valor "2" a minha variável y.

// Então meu sinal de " = " é um operador de atribuição.

// x = x + y 
// x vai receber o valor "7" e não será mais "5".

console.log(x += y) // 7
// É o mesmo que eu falar que a operação é x = x + y.
// x -= y   | x = x - y.
// x *= y   | x = x * y.
// x /= y   | x = x / y.
// x %= y   | x = x % y.
// x **= y  | x = x ** y.

// Incremento
x++ 
console.log(x) // 8
// x++ | x = x + 1

// Decremento 
y--
console.log(y) // 1
// y-- | y = y - 1
